package ru.netology.stats;

import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.Test;

public class StatsServiceTest {
    @Test
    public void totalSellingCountTest() {
        StatsService service = new StatsService();

        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};
        long expected = 8 + 15 + 13 + 15 + 17 + 20 + 19 + 20 + 7 + 14 + 14 + 18;

        long actual = service.totalSellingCount(sales);

        Assertions.assertEquals(expected, actual);
    }

    @Test
    public void averageSellingCountTest() {
        StatsService service = new StatsService();

        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};
        long expected = (8 + 15 + 13 + 15 + 17 + 20 + 19 + 20 + 7 + 14 + 14 + 18) / 12;

        long actual = service.averageSellingCount(sales);

        Assertions.assertEquals(expected, actual);
    }

    @Test
    public void maxAmountMonthTest() {
        StatsService service = new StatsService();

        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};
        long expected = 8;

        long actual = service.maxAmountMonth(sales);

        Assertions.assertEquals(expected, actual);
    }

    @Test
    public void minAmountMonthTest() {
        StatsService service = new StatsService();

        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};
        long expected = 9;

        long actual = service.minAmountMonth(sales);

        Assertions.assertEquals(expected, actual);
    }

    @Test
    public void lessThanAverageTest() {
        StatsService service = new StatsService();

        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};
        long expected = 5;

        long actual = service.lessThanAverage(sales);

        Assertions.assertEquals(expected, actual);
    }

    @Test
    public void moreThanAverageTest() {
        StatsService service = new StatsService();

        long[] sales = {8, 15, 13, 15, 17, 20, 19, 20, 7, 14, 14, 18};
        long expected = 5;

        long actual = service.moreThanAverage(sales);

        Assertions.assertEquals(expected, actual);
    }
}
